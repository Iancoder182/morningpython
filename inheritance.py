class Animal:
    isMammal = True

    def speak(self):
        print("Animal is speaking")

    def move(self):
        print("Animal is moving")

#Child/sub/derived class
class Cat(Animal):
    def sound(self):
        print("cat is meowing")

    def climb(self):
        print("Cat is climbing a tree")

