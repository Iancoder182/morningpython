#Arithmetic Operators
x = 20
y = 6

print(x + y )
print(x - y )
print(x * y)
print(x / y)
print(x % y) #modulus - returns remainder after division

#Comparision Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not equal to

#Assignment Operators - Used to assign/store values in variables
number= 45
print(number)

number += 2
print(number)

#Logical operators - an , or, not
num1 = 67
num2 = 43

print(num1 == num2 and num1> num2)
print(num1 == num2 or num1> num2)
print(not(num1 == num2 or num1> num2))
